def add(number_1, number_2):
  result = number_1 + number_2
  return result

def subtraction(number_1, number_2):
  result = number_1 - number_2
  return result

def multiplication(number_1, number_2):
  result = number_1 * number_2
  return result

def division(number_1, number_2):
  result = number_1 / number_2
  return result

def integer_division(number_1, number_2): # division entière : arrondit à l'entier inférieur
  result = number_1 // number_2
  return result

def modulo(number_1, number_2):
  result = number_1 % number_2
  return result

def exponentiation(number_1, number_2):
  result = number_1 ** number_2
  return result
